package ex_02_Java_Basic;

public class Lab014_printF {
    public static void main(String[] args) {
        int a = 10;
        System.out.printf("Your variable name is  %d\n", a);  // Prints the integer a

        byte b = 10;
        System.out.println();  // Blank line
        System.out.printf("%d", b);  // Prints the byte b
    }
}

